int main(int arg_c, char *arg_v[])
{
   int n = 1;
   const char *filename;

   printf("ELKS PPM Viewer v0.1\n");

   if (arg_c != 2)
      return print_usage();
   
   filename = arg_v[n];

   printf("Source file:      \"%s\"\n", filename);

   sleep(1);
   set_mode(VGA_256_COLOR_MODE);

   int ret = ppm_load_and_display(filename);
   sleep(10);
   set_mode(TEXT_MODE);

   return ret;

   return EXIT_SUCCESS;
}
